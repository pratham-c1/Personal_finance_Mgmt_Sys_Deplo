NEPALI_MONTHS = ['Baisakh','Jestha','Ashadh','Shrawan','Bhadra','Ashwin',
                  'Kartik','Mangsir','Poush','Magh','Falgun','Chaitra']

def get_current_bs():
    """Returns current BS date as dict"""
    from datetime import date
    today = date.today()
    return ad_to_bs(today.year, today.month, today.day)

def ad_to_bs(year, month, day):
    """Convert AD to BS date"""
    # Reference date: 1943-04-14 AD = 2000-01-01 BS
    total_days = 0
    ref_year, ref_month, ref_day = 1943, 4, 14
    
    import datetime
    ref_ad = datetime.date(ref_year, ref_month, ref_day)
    ad_date = datetime.date(year, month, day)
    total_days = (ad_date - ref_ad).days
    
    bs_year = 2000
    bs_month = 1
    bs_day = 1
    
    while total_days > 0:
        days_in_month = BS_CALENDAR.get(bs_year, [30]*12)[bs_month - 1]
        if total_days >= days_in_month:
            total_days -= days_in_month
            bs_month += 1
            if bs_month > 12:
                bs_month = 1
                bs_year += 1
        else:
            bs_day += total_days
            total_days = 0
    
    month_name = NEPALI_MONTHS[bs_month - 1]
    return {
        'year': bs_year,
        'month': bs_month,
        'day': bs_day,
        'month_name': month_name,
        'date_str': f"{bs_year}-{bs_month:02d}-{bs_day:02d}",
        'display': f"{bs_day} {month_name} {bs_year}"
    }
